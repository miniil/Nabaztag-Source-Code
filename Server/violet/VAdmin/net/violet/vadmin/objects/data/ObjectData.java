package net.violet.vadmin.objects.data;


public class ObjectData {

	private String object_id;
	private String object_login;
	private String object_hardware;
	private String object_serial;
	private String object_ping;
	private String object_timezone;
	private String object_private;
	private String object_visible;
	
	
	public String getObject_id() {
		return object_id;
	}
	
	public void setObject_id(String object_id) {
		this.object_id = object_id;
	}
	
	public String getObject_login() {
		return object_login;
	}
	
	public void setObject_login(String object_login) {
		this.object_login = object_login;
	}
	
	public String getObject_hardware() {
		return object_hardware;
	}
	
	public void setObject_hardware(String object_hardware) {
		this.object_hardware = object_hardware;
	}
	
	public String getObject_serial() {
		return object_serial;
	}
	
	public void setObject_serial(String object_serial) {
		this.object_serial = object_serial;
	}
	
	public String getObject_ping() {
		return object_ping;
	}
	
	public void setObject_ping(String object_ping) {
		this.object_ping = object_ping;
	}
	
	public String getObject_timezone() {
		return object_timezone;
	}
	
	public void setObject_timezone(String object_timezone) {
		this.object_timezone = object_timezone;
	}
	
	public String getObject_private() {
		return object_private;
	}
	
	public void setObject_private(String object_private) {
		this.object_private = object_private;
	}
	
	public String getObject_visible() {
		return object_visible;
	}
	
	public void setObject_visible(String object_visible) {
		this.object_visible = object_visible;
	}
	
}
